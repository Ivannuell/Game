class UIStyle:
    def __init__(
        self,
        bg_color,
        border_color,
        hover_color,
        text_color,
        border_width=2,
        font=None
    ):
        self.bg_color = bg_color
        self.border_color = border_color
        self.hover_color = hover_color
        self.text_color = text_color
        self.border_width = border_width
        self.font = font
